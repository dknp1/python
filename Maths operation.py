first_number = int(input('Enter the first number: '))
second_number = int(input('Enter the second number: '))

def func(a, b):
    print('Addition:', a + b)

func(first_number, second_number)

def func2(c, d):
    print('Subtraction:', c - d)

func2(first_number, second_number)

def func3(d, e):
    print('Multiplication:', d * e)

func3(first_number, second_number)

def func4(f, g):
    print('Division:', f / g)

func4(first_number, second_number)
